function add(left, right){
    document.write(left+'+'+right+'=');
    return left + right;
    }
    function sub(left, right){
    document.write(left+'-'+right+'=');
    return left - right;
    }
    function mul(left, right){
    document.write(left+'*'+right+'=');
    return left * right;
    }
    function div(left, right){
    document.write(left+'/'+right+'=');
    return left / right;
    }